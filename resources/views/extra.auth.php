<script>
    let sliderImages = document.querySelectorAll('.hero_slide_background'),
        sliderPrev = document.querySelector('#slide_prev'),
        sliderNext = document.querySelector('#slide_next'),

        current = 0;

        //Clear all images
        function reset(){
            for(let i=0; i<sliderImages.length; i++){
                sliderImages[i].style.display = 'none';
            }
        }


        //Init Slider
        function startSlide()
        {
            reset();
            sliderImages[0].style.display = 'block';
        }


        // Show Prev
        function slidePrev()
        {
            reset();
            sliderImages[current-1].style.display = 'block';
            current--;
        }

        //Show Next

        function slideNext()
        {
            reset();
            sliderImages[current+1].style.display = 'block';
            current++;
        }


         sliderNext.addEventListener('click', function()
        {
            if(current === sliderImages.length - 1)
            {
                current = -1;
            }
             slideNext();
        });


        sliderPrev.addEventListener('click', function()
        {
            if(current === 0)
            {
                current = sliderImages.length;
            }
             slidePrev();
        });

        startSlide();
       

</script>                
                
                
                   
                        
            
                

                

          

